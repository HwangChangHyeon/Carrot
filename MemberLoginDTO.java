package DAODTO;

public class MemberLoginDTO {
	private String id;
	private String pw;
	private String check;
	
	public String getid() {
		return id;
	}
	public void setid(String id) {
		this.id = id;
	}
	public String getpw() {
		return pw;
	}
	public void setpw(String pw) {
		this.pw = pw;
	}
	
	public String getcheck() {
		return check;
	}
	public void setcheck(String check) {
		this.check = check;
	}
}
